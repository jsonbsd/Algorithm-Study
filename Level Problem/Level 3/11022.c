int main(int argc, const char *argv[]) {
    int n_test, a, b;
    int i;
    
    scanf("%d", &n_test);
    
    for (i = 1; i <= n_test; ++i) {
        scanf("%d %d", &a, &b);
        printf("Case #%d: %d + %d = %d\n", i, a, b, a + b);
    }

    return 0;
}
